    public static String combine(String... source) {
        StringBuffer buffer = new StringBuffer();
        for (String s : source) {
            buffer.append(s).append(UNDER_LINE);
        }
        buffer.deleteCharAt(buffer.length() - 1);
        return buffer.toString();
    }

    public static String combinePkgName(String pkgName, String timeStamp) {
        StringBuffer buffer = new StringBuffer();
        buffer.append(pkgName).append(PACKAGE_COMBINE_SYMBOL).append(timeStamp);
        return buffer.toString();
    }

